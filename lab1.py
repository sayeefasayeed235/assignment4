def mark_sorting(N,id,mark):
   id=list(map(int,id))
   mark=list(map(int,mark))
   count=0
   for i in range(N):
      high_id=i
      for j in range(i+1,N):
         if mark[j]>mark[high_id]:
            high_id=j
         elif mark[j]==mark[high_id]:
            if id[j]<id[high_id]:
             high_id=j
      if high_id!=i:
         mark[i],mark[high_id]=mark[high_id],mark[i]
         id[i],id[high_id]=id[high_id],id[i]
         count+=1
   print(f"Minimum swaps: {count}")
   for i in range(N):
      print(f"ID: {id[i]} Mark: {mark[i]}")





N=int(input())
