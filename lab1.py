def bubblesort(N,arr):
  arr = list(map(int,arr))
  for i in range(N-1):
    flag=False
    for j in range(N-1-i):
      if arr[j]>arr[j+1]:
        arr[j],arr[j+1]=arr[j+1],arr[j]
        flag=True
    if flag==False:
      break
  for i in range(N):
    print(arr[i],end=" ")

N=int(input())
